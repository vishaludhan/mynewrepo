package conditinalStatements;

public class SwitchCaseUse {

	public static void main(String[] args) {
		String month="may";
		
		
	switch (month) {
	case "jan":
		
		System.out.println("you are in 1st month");
		
		break;
	case "feb":
		System.out.println("you are in 2nd month");
		break;
		
	case "march":
		System.out.println("you are in 3rd month");
		break;
	case "april":
		System.out.println("you are in 4th month");
		break;
	case "may":
		System.out.println("you are in 5th month");
		break;
	case "jun":
		System.out.println("you are in 6th month");
		break;
	case "july":
		System.out.println("you are in 7th month");
		break;
	case "augest":
		System.out.println("you are in 8th month");
		break;
	case "sep":
		System.out.println("you are in 9th month");
		break;
	case "octomber":
		System.out.println("you are in 10th month");
		break;
	case "nov":
		System.out.println("you are in 11th month");
		break;
	case "dec":
		System.out.println("you are in 12th month");
		break;

	default:
		System.out.println("wrong data");
		break;
	}
          
	}

}







