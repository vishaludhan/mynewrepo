package loopUse;

public class WhileLoopUse {

	public static void main(String[] args)
	{
	
		int a=18;
		
	while (a<=180) {
		
		
		System.out.println(a);
		a=a+18;
		
	}
	System.out.println("=====================================================");
	int b=180;
	while(b>=18) {

		System.out.println(b);
		b=b-18;
		
		
	}System.out.println("=======================================================");
	}
	

}
